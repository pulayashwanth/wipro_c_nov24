// loops 

// C provides three types of loops.


//  for Loop
// Use Case: When the number of iterations is known beforehand.

// Syntax:



// for (initialization; condition; increment/decrement) {

//     // Code to execute

// }


// 2. while Loop

// Use Case: When the number of iterations is unknown, but the loop depends on a condition.


// Syntax:

// while (condition) {
//     // Code to execute
// }

// The condition is checked before each iteration.



//  do-while Loop

// Use Case: When the loop must execute at least once, regardless of the condition.

// do {
//     // Code to execute
// } while (condition);


// Execution Flow:

// The loop body executes first.
// The condition is checked after execution.
// If the condition is true, the loop repeats

